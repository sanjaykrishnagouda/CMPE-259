void reverse_bytes(unsigned long *p)
{
    unsigned long UL = *p;

//    printf("%f", *((double *) (&UL)));
    UL = (UL & 0x00ff00ff00ff00ffUL) <<  8 | (UL & 0xff00ff00ff00ff00UL) >>  8;
    UL = (UL & 0x0000ffff0000ffffUL) << 16 | (UL & 0xffff0000ffff0000UL) >> 16;
    UL = (UL & 0x00000000ffffffffUL) << 32 | (UL & 0xffffffff00000000UL) >> 32;
//    printf(" --> %f\n", *((double *) (&UL)));

    *p = UL;
}


